package org.casio.calculatorapp.calculator;

public class Calculator {

	int add(int a, int b) {
		int c = a + b;
		return c;
	}

	int mul(int a1, int b1) {
		int c1 = a1 * b1;
		return c1;

	}

	int sub(int a2, int b2) {
		int c2 = a2 - b2;
		return c2;
	}

	float div(float a3, float b3) {
		float c3 = a3 / b3;
		return c3;
	}

	int square(int g) {
		int h = g * g;
		return h;
	}
	
	int cube(int e) {
		int f=e*e*e;
		return f;
	}
}
